
public class Student {
int roll;
int add;
public Student(int roll, int add) {
	super();
	this.roll = roll;
	this.add = add;
}
public int getRoll() {
	return roll;
}
public void setRoll(int roll) {
	this.roll = roll;
}
public int getAdd() {
	return add;
}
public void setAdd(int add) {
	this.add = add;
}


}
